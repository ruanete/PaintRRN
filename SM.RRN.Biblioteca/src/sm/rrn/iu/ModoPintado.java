package sm.rrn.iu;

/**
 * Enumerado que define los modos de pintado
 * @author Raúl Ruano Narváez
 */
public enum ModoPintado {

    /**
     * Modo PUNTO
     */
    Punto,

    /**
     * Modo LÍNEA
     */
    Linea,

    /**
     * Modo RECTANGULO
     */
    Rectangulo,

    /**
     * Modo ELIPSE
     */
    Elipse,

    /**
     * Modo EDICIÓN
     */
    Edicion,

    /**
     * Modo ARCO
     */
    Arco,

    /**
     * Modo RECTANGULO REDONDEADO
     */
    RoundedRectangle,

    /**
     * Modo CURVA CON UN PUNTO DE CONTROL
     */
    QuadCurve,

    /**
     * Modo CURVA CON DOS PUNTOS DE CONTROL
     */
    CubicCurve,

    /**
     * Modo TRAZO LIBRE
     */
    TrazoLibre,

    /**
     * Modo POLÍGONO
     */
    Poligono,

    /**
     * Modo ESTRELLA
     */
    FormaPersonalizada,

    /**
     * Modo TEXTO
     */
    Texto
}
